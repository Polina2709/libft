#include "libft.h"

int	ft_atoi(const char *str)
{
	int a;
	int nb;

	a = 1;
	nb = 0;
	while (*str == '\n' || *str == '\t' || *str == '\r' || *str == '\v' ||
			*str == '\f' || *str == ' ')
		str++;
	if (*str == '-')
	{
		a = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	return (nb * a);
}
